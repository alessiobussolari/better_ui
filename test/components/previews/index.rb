# @title BetterUI - Catalogo Componenti
# @label_key name
#
# Benvenuto nel catalogo dei componenti BetterUI! Questa libreria offre componenti UI riutilizzabili
# per le tue applicazioni Rails, costruiti con ViewComponent e Tailwind CSS.
#
# ## Componenti disponibili
#
# Esplora i componenti disponibili nella barra laterale di navigazione. I componenti sono organizzati in categorie:
#
# - **Componenti Generali**: Componenti di base come bottoni, breadcrumb, tabelle e icone
# - **Componenti Applicativi**: Componenti specifici per applicazioni come card, alert, ecc.
#
# ## Come usare i parametri
#
# Per ogni componente, puoi modificare i parametri nella scheda "Params" per vedere come cambia l'aspetto del componente.
# Ad esempio, per il componente Breadcrumb puoi cambiare:
#
# - **variant**: default, primary, light
# - **separator**: chevron, slash, arrow, dot, pipe
# - **size**: sm, md, lg
# - **inverse**: true, false
# - **with_icons**: true, false
# - **items_count**: 2, 3, 5
#
# ## Note sui tipi di dati
#
# - I parametri di tipo Symbol devono essere inseriti senza i due punti (es. "default" invece di ":default")
# - I parametri Boolean accettano "true" o "false"
# - I parametri Integer sono numeri interi (es. 3)
